# 2. 학과 정보를 딕셔너리로 저장

# 3. 학생 정보를 저장할 빈 리스트 생성

# 4. 5명의 학생 정보 입력 받기
# 이름, 학번, 평균 학점, 수강 과목을 입력 받아 딕셔너리로 저장 후 리스트에 추가
# 수강 과목은 콤마로 구분된 입력을 받아 튜플로 변환

# 5. 학과 정보 처리
# 학과명과 설립 연도 연결, 학과명 반복, 학과명 길이 계산

# 6. 학생 정보 처리
# 첫 번째와 마지막 학생 정보 출력
# 2~4번째 학생 정보 출력
# 새로운 학생 추가 및 첫 번째 학생 삭제

# 7. 학과 통계 계산
# 평균 학점의 평균 계산
# 3.5 이상 학점 학생 수 계산
# 최고 및 최저 평균 학점 찾기

# 8. 추가 기능 구현
# 학과 정보에 인증 여부 추가
# 특정 학생 정보 검색 기능 구현

# 9. 모든 정보 출력
# 처리된 학과 정보, 학생 정보, 통계 결과 출력
